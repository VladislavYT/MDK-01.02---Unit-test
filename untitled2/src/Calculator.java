public class Calculator {
    public int plus(int a, int b){
        int c = a+b;
        System.out.println(c);
        return c;
    }
    public int minus(int a, int b){
        int c = a-b;
        System.out.println(c);
        return c;
    }
    public int multi(int a, int b){
        int c = a*b;
        System.out.println(c);
        return c;
    }
    public int division(int a, int b){
        int c = a/b;
        System.out.println(c);
        return c;
    }
    public int perimeter(int a){
        int c = a*4;
        System.out.println(c);
        return c;
    }
}